export async function queryTicket(
  db: D1Database,
  apiKey: string,
  question: string,
  source: string = 'admin',
  lineUserId: string | null = null,
): Promise<string> {
  const callGroq = () => fetch('https://api.groq.com/openai/v1/chat/completions', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${apiKey}`,
    },
    body: JSON.stringify({
      model: 'llama-3.3-70b-versatile',
      messages: [
        { role: 'system', content: SYSTEM_PROMPT },
        { role: 'user', content: question },
      ],
      temperature: 0,
      max_tokens: 1024,
    }),
  });

  let aiRes = await callGroq();

  // レート制限時は待って1回だけリトライ（GroqのTPM制限は分単位で回復するため長めに待つ）
  if (aiRes.status === 429) {
    await new Promise(r => setTimeout(r, 10000));
    aiRes = await callGroq();
  }

  if (!aiRes.ok) {
    const errText = await aiRes.text();
    console.error('Groq API error (ticket_bot)', aiRes.status, errText);
    return `AI応答エラー(${aiRes.status})：時間をおいて再度お試しください。急ぎの場合は営業所に確認してください。`;
  }

  const aiJson = await aiRes.json() as { choices: { message: { content: string } }[] };
  const answer = aiJson.choices?.[0]?.message?.content ?? '回答を生成できませんでした。営業所に確認してください。';

  await db.prepare(
    `INSERT INTO manual_chat_logs (source, line_user_id, question, answer) VALUES (?, ?, ?, ?)`
  ).bind(`${source}_ticket`, lineUserId, question, answer).run().catch(() => {});

  return answer;
}
