CREATE TABLE IF NOT EXISTS tantosha_groups (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT NOT NULL,
  month_label TEXT DEFAULT '',
  note TEXT DEFAULT '',
  sort_order INTEGER DEFAULT 0,
  is_active INTEGER DEFAULT 1,
  updated_at TEXT DEFAULT (datetime('now','localtime'))
);

CREATE TABLE IF NOT EXISTS tantosha_rows (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  group_id INTEGER NOT NULL,
  sort_order INTEGER DEFAULT 0,
  shift TEXT DEFAULT '',
  door TEXT DEFAULT '',
  row_color TEXT DEFAULT '',
  p1_letter TEXT DEFAULT '', p1_name TEXT DEFAULT '', p1_badge TEXT DEFAULT '', p1_color TEXT DEFAULT '', p1_hl TEXT DEFAULT '',
  p2_letter TEXT DEFAULT '', p2_name TEXT DEFAULT '', p2_badge TEXT DEFAULT '', p2_color TEXT DEFAULT '', p2_hl TEXT DEFAULT '',
  r_letter TEXT DEFAULT '', r_name TEXT DEFAULT '', r_badge TEXT DEFAULT '', r_color TEXT DEFAULT '', r_hl TEXT DEFAULT ''
);
CREATE INDEX IF NOT EXISTS idx_tantosha_rows_group ON tantosha_rows(group_id, sort_order);

CREATE TABLE IF NOT EXISTS tantosha_side (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  group_id INTEGER NOT NULL,
  section TEXT NOT NULL,
  sort_order INTEGER DEFAULT 0,
  col1 TEXT DEFAULT '',
  col2 TEXT DEFAULT '',
  name TEXT DEFAULT '',
  badge TEXT DEFAULT '',
  color TEXT DEFAULT '',
  hl TEXT DEFAULT ''
);
CREATE INDEX IF NOT EXISTS idx_tantosha_side_group ON tantosha_side(group_id, sort_order);

CREATE TABLE IF NOT EXISTS tantosha_edit_logs (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  admin_id INTEGER,
  admin_name TEXT,
  action TEXT,
  target TEXT,
  detail TEXT,
  created_at TEXT DEFAULT (datetime('now','localtime'))
);

