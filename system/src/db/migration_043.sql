-- ===================================================
-- migration_043: 勤務ダイヤマスター + サイクルマスター
--   紙の「ダイヤマスター一覧表」「サイクル一覧表」（板橋）を
--   設定ページから編集できるようにするためのテーブル。
--   時刻は "HH:MM" のテキスト。24時越え表記（例 29:00 = 翌5時）も許容する。
--   dia_cycles.pattern は日ごとの表示記号のJSON配列（空文字 = 空欄。隔日勤務の明け番など）
-- ===================================================

CREATE TABLE IF NOT EXISTS dia_master (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  code INTEGER NOT NULL UNIQUE,            -- コード
  category TEXT NOT NULL DEFAULT '出勤',   -- ダイヤ区分（出勤/公出/公休/W公休/欠勤/病欠/有給/特休）
  days REAL NOT NULL DEFAULT 1.0,          -- 日数（隔日勤務=2.0, 日勤=1.0, 公休=0.0）
  name TEXT NOT NULL,                      -- ダイヤ名（A勤 など）
  symbol TEXT NOT NULL DEFAULT '',         -- 表示記号（A など）
  -- 拘束
  kosoku_start TEXT NOT NULL DEFAULT '00:00',
  kosoku_end   TEXT NOT NULL DEFAULT '00:00',
  kosoku_time  TEXT NOT NULL DEFAULT '00:00',
  -- 所定
  shotei_start TEXT NOT NULL DEFAULT '00:00',
  shotei_end   TEXT NOT NULL DEFAULT '00:00',
  shotei_time  TEXT NOT NULL DEFAULT '00:00',
  -- 残業
  zangyo_start TEXT NOT NULL DEFAULT '00:00',
  zangyo_end   TEXT NOT NULL DEFAULT '00:00',
  zangyo_time  TEXT NOT NULL DEFAULT '00:00',
  -- 深夜
  shinya_start TEXT NOT NULL DEFAULT '00:00',
  shinya_end   TEXT NOT NULL DEFAULT '00:00',
  shinya_time  TEXT NOT NULL DEFAULT '00:00',
  -- 休憩1〜4
  kyukei1_start TEXT NOT NULL DEFAULT '00:00', kyukei1_end TEXT NOT NULL DEFAULT '00:00', kyukei1_time TEXT NOT NULL DEFAULT '00:00',
  kyukei2_start TEXT NOT NULL DEFAULT '00:00', kyukei2_end TEXT NOT NULL DEFAULT '00:00', kyukei2_time TEXT NOT NULL DEFAULT '00:00',
  kyukei3_start TEXT NOT NULL DEFAULT '00:00', kyukei3_end TEXT NOT NULL DEFAULT '00:00', kyukei3_time TEXT NOT NULL DEFAULT '00:00',
  kyukei4_start TEXT NOT NULL DEFAULT '00:00', kyukei4_end TEXT NOT NULL DEFAULT '00:00', kyukei4_time TEXT NOT NULL DEFAULT '00:00',
  -- 指導基準
  std_eishu      INTEGER NOT NULL DEFAULT 0,   -- 営収（円）
  std_run_max    INTEGER NOT NULL DEFAULT 0,   -- 走行上限（km）
  std_run_min    INTEGER NOT NULL DEFAULT 0,   -- 走行下限（km）
  std_kosoku_max TEXT NOT NULL DEFAULT '00:00', -- 拘束 上限
  std_kosoku_min TEXT NOT NULL DEFAULT '00:00', -- 拘束 下限
  std_handle_max TEXT NOT NULL DEFAULT '00:00', -- ハンドル 上限
  std_handle_min TEXT NOT NULL DEFAULT '00:00', -- ハンドル 下限
  std_kutei_max  TEXT NOT NULL DEFAULT '00:00', -- 空停 上限
  std_kutei_min  TEXT NOT NULL DEFAULT '00:00', -- 空停 下限
  is_active INTEGER NOT NULL DEFAULT 1,
  updated_at TEXT DEFAULT (datetime('now', 'localtime'))
);

CREATE TABLE IF NOT EXISTS dia_cycles (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  cycle_no INTEGER NOT NULL UNIQUE,  -- サイクル番号
  name TEXT NOT NULL,                -- サイクル名（B勤 / 3・2T2・2指(B) など）
  days INTEGER NOT NULL DEFAULT 1,   -- 日数
  pattern TEXT NOT NULL DEFAULT '[]',-- 日ごとの表示記号のJSON配列
  is_active INTEGER NOT NULL DEFAULT 1,
  updated_at TEXT DEFAULT (datetime('now', 'localtime'))
);

